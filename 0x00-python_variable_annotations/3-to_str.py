#!/usr/bin/env python3
"""
This module defines a type-annotated function
for converting a float to its string representation.
"""


def to_str(n: float) -> str:
    """
    Returns the string representation of a float.

    Args:
        n (float): The float number to convert to a string.

    Returns:
        str: The string representation of the float.
    """
    return str(n)
