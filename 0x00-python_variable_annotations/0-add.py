#!/usr/bin/env python3
"""
This module defines a type-annotated function for adding two floats.
"""


def add(a: float, b: float) -> float:
    """
    Adds two floating-point numbers and returns the sum.

    Args:
        a (float): The fisrt number.
        b (float): The second number.

    Returns:
        float: The sum of a and b.
    """
    return a + b
